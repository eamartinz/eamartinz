# Tipos int e float no Python
# int -> Números inteiros (ex: -10, 0, 25)
# O tipo int represnta números inteiros positivos e negativos, sem parte decimal.
print(10)    # Exemplo de int positivo
print(-5)    # Exemplo de int negativo
print(0)     # Exemplo de zero, que também é um int

# float -> Números decimais ou de ponto flutuante (ex: -3.14, 0.0, 2.718)
# O tipo float representa números com parte decimal, tanto positivos quanto negativos.
print(3.14)   # Exemplo de float positivo
print(-0.001) # Exemplo de float negativo
print(0.0)    # Exemplo de float zero

# A função type mostra o tipo de dado de uma variável ou valor.
print(type(10))      # Saída: <class 'int'>
print(type(-5))      # Saída: <class 'int'>
print(type(3.14))    # Saída: <class 'float'>
print(type(-0.001))  # Saída: <class 'float'>
print(type('Eduardo'))  # Saída: <class 'str'>