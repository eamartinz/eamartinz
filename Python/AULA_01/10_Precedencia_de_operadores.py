# Precedência de operadores matemáticos em Python
# Ordem de precedência:
# 1. Parênteses ()
# 2. Exponenciação **
# 3. Multiplicação * e Divisão / e Resto %
# 4. Adição + e Subtração - 
# Operadores com a mesma precedência são avaliados da esquerda para a direita
# Exemplo 1
resultado1 = 3 + 5 * 2
print("Resultado 1 (3 + 5 * 2):", resultado1)
# Exemplo 2
resultado2 = (3 + 5) * 2
print("Resultado 2 ((3 + 5) * 2):", resultado2)
# Exemplo 3
resultado3 = 2 ** 3 * 4 + 1
print("Resultado 3 (2 ** 3 * 4 + 1):", resultado3)
# Exemplo 4
resultado4 = 10 - 2 ** 2 + 3 * 2
print("Resultado 4 (10 - 2 ** 2 + 3 * 2):", resultado4)
# Exemplo 5
resultado5 = 18 / 3 + 2 * (4 - 1)
print("Resultado 5 (18 / 3 + 2 * (4 - 1)):", resultado5)
# Exemplo 6
resultado6 = 5 + 3 * 2 ** 2 - (4 / 2)
print("Resultado 6 (5 + 3 * 2 ** 2 - (4 / 2)):", resultado6)
# Exemplo 7
resultado7 = (8 + 2) * (5 - 3) ** 2
print("Resultado 7 ((8 + 2) * (5 - 3) ** 2):", resultado7)
# Exemplo 8
resultado8 = 20 / 4 + 3 * 2 - 1
print("Resultado 8 (20 / 4 + 3 * 2 - 1):", resultado8)
# Exemplo 9
resultado9 = 6 + 4 * 2 ** 3 / 2 - 5
print("Resultado 9 (6 + 4 * 2 ** 3 / 2 - 5):", resultado9)
# Exemplo 10
resultado10 = (7 - 2) * 3 + 4 ** 2 / 2
print("Resultado 10 ((7 - 2) * 3 + 4 ** 2 / 2):", resultado10)