# Concatenaçao e repetição em Python
# Em Python, podemos concatenar (juntar) strings usando o operador +
# Também podemos repetir strings usando o operador *

# Exemplo de concatenação
nome = 'Eduardo'
sobrenome = 'Martins'
nome_completo = nome + ' ' + sobrenome
print('Nome completo:', nome_completo)

palavra = 'Eduardo' + str(1) + 'Martins'
print('Palavra concatenada:', palavra)

# A repetição de strings é feita com o operador *
repeticao = 'Python! ' * 3
print('Repetição de string:', repeticao)