# Conversão (ou coerção) de tipos de dados em Python
# Às vezes, precisamos converter um tipo de dado em outro.
# Python oferece funções embutidas para isso.
# int() -> Converte um valor para inteiro
# float() -> Converte um valor para ponto flutuante (decimal)
# str() -> Converte um valor para string (texto)
# bool() -> Converte um valor para booleano (True ou False)

# Exemplo de conversão para int
print(int(3.14))    # Converte float para int, resultado: 3
print(int("42"))    # Converte string para int, resultado: 42
print(int(True))    # Converte bool para int, resultado: 1
print(int(False))   # Converte bool para int, resultado: 0