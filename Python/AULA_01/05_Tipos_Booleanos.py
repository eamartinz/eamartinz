# Tipos de dados booleanos em Python
# bool -> Valores booleanos (True ou False)
# O tipo bool representa valores lógicos, que podem ser apenas True (verdadeiro) ou False (falso).
print(True)   # Exemplo de valor booleano True
print(False)  # Exemplo de valor booleano False

# Existem várias maneiras de obter valores booleanos em Python.
# Comparações retornam valores booleanos.
print(5 > 3)    # True, porque 5 é maior que 3
print(2 == 2)   # True, porque 2 é igual a 2
print(4 < 1)    # False, porque 4 não é menor que 1
print(3 != 3)   # False, porque 3 é igual a 3

print(type(True))   # Saída: <class 'bool'>
print(type(False))  # Saída: <class 'bool'>
print(type(10 == 10))  # Saída: <class 'bool'>
print(type(5 < 2))     # Saída: <class 'bool'>
